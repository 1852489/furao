package demo.controller;

public class old_grade {
    int old;
    double grade;

    public old_grade(int old, double grade) {
        this.old = old;
        this.grade = grade;
    }

    public int getOld() {
        return old;
    }

    public void setOld(int old) {
        this.old = old;
    }

    public double getGrade() {
        return grade;
    }

    public void setGrade(double grade) {
        this.grade = grade;
    }
}
